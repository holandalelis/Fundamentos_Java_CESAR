package school.cesar.fundamentosjava.basico;

public class Cast {

    public static void main(String[] args) {
        /*
         * crie um int a e atribua um valor
         * crei um short b e atribua o int a
         * faca o um cast :-)
         */

        int a = 123456789;   // 2 bytes
        short c = (short) a;
        System.out.println(c);
    }
}
